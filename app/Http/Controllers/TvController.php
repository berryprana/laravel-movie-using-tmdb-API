     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $tvdetails = Http::withToken(config('services.tmdb.token'))
        ->get('https://api.themoviedb.org/3/tv/'.$id.'?append_to_response=credits,videos,images')
        ->json();

        $translations = Http::withToken(config('services.tmdb.token'))
        ->get('https://api.themoviedb.org/3/tv/'.$id.'/translations')
        ->json()['translations'];
        
        // dump($translations);

        $viewModel = new ShowTvViewModel(
            $tvdetails
        );

        return view('tv.show', $viewModel);
    }

